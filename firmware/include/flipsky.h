// ── Commandes UART ────────────────────────────────────────────────────────────
#define FTESC_OBTAIN_DATA             0   // demander télémétrie (Input Signal Type: any)
#define FTESC_CONTROL_AND_OBTAIN      2   // contrôle + télémétrie (Input Signal Type: UART)
#define FTESC_SET_DUTY                3   // duty cycle    — seulement si IS_TYPE = OFF
#define FTESC_SET_CURRENT             4   // courant moteur — seulement si IS_TYPE = OFF
#define FTESC_SET_BRAKE_CURRENT       6   // courant frein  — seulement si IS_TYPE = OFF
#define FTESC_KEEP_ALIVE              25  // heartbeat

// Throttle pour cmd 02H
#define FTESC_THROTTLE_NEUTRAL  512   // 0 vitesse
#define FTESC_THROTTLE_MAX      1023  // pleine vitesse avant
#define FTESC_THROTTLE_MIN      0     // frein maximal

// Gear pour D6 de cmd 02H
#define FTESC_GEAR_NONE     0x00
#define FTESC_GEAR_LOW      0x01
#define FTESC_GEAR_MEDIUM   0x02
#define FTESC_GEAR_HIGH     0x03
#define FTESC_GEAR_REVERSE  0x04

// ── Structure données temps réel ──────────────────────────────────────────────
